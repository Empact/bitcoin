#ifndef BITCOIN_UTILERROR_H
#define BITCOIN_UTILERROR_H

#include <logging.h>
#include <tinyformat.h>

template<typename... Args>
bool error(const char* fmt, const Args&... args)
{
    LogPrintf("ERROR: %s\n", tfm::format(fmt, args...));
    return false;
}

#endif // BITCOIN_UTILERROR_H
